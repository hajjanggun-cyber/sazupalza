// 획수 추정 함수
export function estimateStrokeCount(char: string): number {
  // 한자 먼저 체크
  if (hanjaStrokeCount[char]) return hanjaStrokeCount[char];
  // 한글 체크
  if (koreanStrokeCount[char]) return koreanStrokeCount[char];
  // 기본값: 글자 코드 기반 추정
  const code = char.charCodeAt(0);
  if (code >= 0xAC00 && code <= 0xD7A3) {
    // 한글 음절 - 복잡도 기반 추정
    return Math.floor((code - 0xAC00) / 2000) + 4;
  }
  return 8; // 기본값
}
